제 1조 (목적)

본 약관(이하 "약관")은 (주)미리내닷컴(이하 "회사")과 회원에 관한 제반사항을 규정함을 목적으로 합니다.

제 2조 (용어의 정의)

'회원'은 회사에 개인정보를 제공하여 회원등록을 한 자로서, 회사가 제공하는 서비스를 계속적으로 이용할 수 있는 자를 말합니다.

제 3조 (약관의 게시 및 변경)

① 회사는 약관의 규제에 관한 법률, 전자거래기본법, 정보통신망이용촉진 등에 관한 법률, 소비자보호법 등 관련법을 위배하지 않는 범위에서 이 약관을 개정할 수 있습니다. 
② 회사가 약관을 개정할 경우에는 적용일자 및 개정사유를 명시하여 회사 홈페이지의 보기 쉬운 곳에 그 적용일자 7일 이전부터 적용일자 전일까지 공지합니다. 
③ 전항의 방법으로 변경 고지된 약관은 기존 회원에게도 유효하게 효력이 발생합니다.
④ 이 약관에서 정하지 아니한 사항과 이 약관의 해석에 관하여는 관계법령 또는 상관례에 따릅니다.

제 4조 (약관 외 준칙 및 관할법원)

① 본 약관에 명시되지 않은 사항은 국내 관계법에 준하여 해석합니다.
② 회원과 회사 사이에 분쟁이 발생할 경우 서울민사지방법원을 관할 법원으로 합니다.

제 5조 (회원 가입 및 자격)

① 회사가 정한 양식에 따라 회원정보를 기입한 후 회원가입을 신청함으로써 회원으로 등록됩니다.
② 다음 각 호의 1에 해당하는 경우, 회사가 임의적으로 회원가입을 인정하지 않거나 회원자격을 박탈할 수 있습니다. 
1.다른 사람의 명의를 사용하여 신청하였을 경우 
2.신청 시 필수 내용을 허위로 기재하여 신청하였을 경우 
3.사회의 안녕질서 또는 미풍양속을 저해할 목적으로 신청하였을 경우 
4.다른 사람의 회사의 이용을 방해하거나 그 정보를 도용하는 등 전자거래질서를 위협하는 경우 
5.회사를 이용하여 법령과 이 약관이 금지하거나 풍속에 반하는 행위를 하는 경우 
③ 회사가 회원자격을 박탈 시키는 경우에는 회원등록을 말소합니다. 이 경우 회원에게 이를 통지하고, 회원등록 말소 전에 소명할 기회를 부여합니다.

제 6조 (개인정보의 취득 및 이용)

① 회사는 고객의 개인정보의 취득과 이용, 보호 등에 관한 사항을 "개인정보보호정책"에 정한 바에 따르며, 개인정보보호정책은 홈페이지 하단에 상시적으로 게시합니다.
② 회사는 고객으로부터 각종 개인신상에 관한 정보를 제공받을 수 있으며 제공받은 정보는 회사가 제공하는 서비스를 고객이 효율적으로 이용할 수 있도록 하는데 사용될 수 있습니다.
③ 회사는 고객으로부터 취득한 신상정보를 고객의 사전 동의 없이 제 3자에게 제공할 수 없습니다. 단, 다음 각 호의 1에 해당하는 경우에는 예외로 합니다. 
1.도메인 이름에 대한 WHOIS 서비스를 제공하는 경우 (.kr과 같은 형태의 국내도메인의 경우에는 고객이 도메인 등록시 개인정보공개를 동의한 경우에만 제공합니다.) 
2.전기통신기본법 등 관계법령에 의하여 국가기관의 요청에 의한 경우 
3.범죄에 대한 수사상의 목적이 있거나 정보통신윤리위원회의 요청이 있는 경우 
4.업무상 연락을 위하여 회원의 정보(성명, 주소, 전화번호)를 사용하는 경우 
5.은행업무상 관련사항에 한하여 일부 정보를 공유하는 경우 
6.통계작성, 홍보자료, 학술연구 또는 시장조사를 위하여 필요한 경우로서 특정고객 임을 식별할 수 없는 형태로 제공되는 경우

제 7조 (회원 탈퇴)

① 회원은 회사에 언제든지 탈퇴를 요청할 수 있으며 회사는 지체없이 회원탈퇴를 처리합니다. 
② 회원이 회사의 무료 호스팅 서비스, 유료 서비스를 이용할 경우 이용중인 서비스가 해지완료 되지 않았거나 도메인 서비스 만기일이 지나지 않은 경우에는 본 약관 제8조에 따라 고객에게 해당 내용을 통지후 탈퇴 처리 하지 않습니다.
③ 본 조 2항의 서비스가 해지된 경우에도 고객이 회사에 납입해야하는 서비스 이용요금이 있을 경우 해당 요금이 완전 처리되는 시점까지 회원탈퇴는 유보됩니다. 단, 고객이 해당 요금을 완납한 경우에는 지체없이 회원탈퇴를 처리합니다.

제 8조 (회원에 대한 통지)

① 회사가 회원에 대한 통지를 하는 경우, 회원이 회사에 제출한 전자우편 주소로 할 수 있습니다. 
② 회사는 불특정다수 회원에 대한 통지의 경우 1주일 이상 회사 홈페이지 게시판에 게시함으로서 개별 통지에 갈음할 수 있습니다.

제 9조 (회사의 의무)

① 회사는 본 약관이 정하는 바에 따라 지속적이고 안정적인 서비스를 제공하는데 최선을 다하여야 합니다.
② 회사는 항상 등록자의 정보를 포함한 개인신상정보에 대하여 관리적, 기술적 안전조치를 강구하여 정보보안에 최선을 다하여야 합니다.
③ 회사는 공정하고 건전한 운영을 통하여 전자상거래 질서유지에 최선을 다하고 지속적인 연구개발을 통하여 양질의 서비스를 제공함으로써 고객만족을 극대화하여 인터넷 사업 발전에 기여합니다.
④ 회사는 고객으로부터 제기되는 불편사항 및 문제에 대해 정당하다고 판단될 경우 우선적으로 그 문제를 지체없이 처리해야 합니다. 단, 신속한 처리가 곤란할 경우, 고객에게 그 사유와 처리일정을 통지하여야 합니다.
⑤ 회사는 소비자 보호단체 및 공공기관의 소비자 보호업무의 추진에 필요한 자료 등의 요구에 적극 협력합니다.

제 10조 (회원의 의무)

① ID와 비밀번호에 관한 모든 관리의 책임은 회원에게 있습니다. 
② 회원은 ID와 비밀번호를 제 3자가 알 수 있도록 해서는 안 됩니다. 
③ 회원은 본 약관 및 관계법령에서 규정한 사항을 준수해야 합니다. 
④ 다음 각 호의 1에 해당하는 행위를 할 경우 이용권리가 박탈됩니다. 
1.범죄와 결부된다고 객관적으로 판단되는 행위 
2.기타 관계법령에 위배되는 행위

